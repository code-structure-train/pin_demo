void led1_thread_entry(void* parameter)
{
  while(1) {
    HAL_GPIO_WritePin(LED_G_GPIO_Port, LED_G_Pin, GPIO_PIN_RESET);
    rt_thread_delay(500);

    HAL_GPIO_WritePin(LED_G_GPIO_Port, LED_G_Pin, GPIO_PIN_SET);
    rt_thread_delay(500);
  }
}

